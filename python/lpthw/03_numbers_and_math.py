# In python3, print has been replaced by print()

print ("I will now count my chickens")

print ("Hens:", 25 + 30/6)
print ("Roosters:", 100 - 25 * 3 % 4)

print ("Now I will count the eggs")
print ("Eggs:", 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6)

print (3 + 2 < 5 - 7)

print ("What is 3 + 2?", 3 + 2)
print ("What is 5 - 7?", 5 - 7)
# by default, the , concatenator adds a space between the concatenated parts.
# To avoid the space, define a separator (sep) as sep = ""
print ("Oh, that's why 3 + 2 < 5 - 7 returns ", 3 + 2 < 5 - 7, "!", sep="")
